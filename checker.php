    <script>
        // Navigation
        function showDashboard() {
            document.getElementById('dashboard').style.display = 'block';
            document.getElementById('checker').style.display = 'none';
            document.querySelector('.nav-link[href="#dashboard"]').classList.add('active');
            document.querySelector('.nav-link[href="#checker"]').classList.remove('active');
        }

        function showChecker() {
            document.getElementById('dashboard').style.display = 'none';
            document.getElementById('checker').style.display = 'block';
            document.querySelector('.nav-link[href="#dashboard"]').classList.remove('active');
            document.querySelector('.nav-link[href="#checker"]').classList.add('active');
        }

        // BIN Lookup
        function showBinLookup() {
            new bootstrap.Modal(document.getElementById('binModal')).show();
        }

        function testBin(bin) {
            const binDatabase = {
                '411111': { brand: 'VISA', type: 'CREDIT', level: 'CLASSIC', bank: 'Chase Bank', country: 'United States' },
                '555555': { brand: 'MASTERCARD', type: 'CREDIT', level: 'STANDARD', bank: 'Bank of America', country: 'United States' },
                '378282': { brand: 'AMERICAN EXPRESS', type: 'CREDIT', level: 'GOLD', bank: 'American Express', country: 'United States' }
            };

            const info = binDatabase[bin];
            const resultDiv = document.getElementById('testResult');
            
            if (info) {
                resultDiv.innerHTML = `
                    <div class="alert alert-success small">
                        <strong>${info.brand}</strong> ${info.type}<br>
                        <small>${info.bank} - ${info.country}</small>
                    </div>
                `;
            } else {
                resultDiv.innerHTML = '<div class="alert alert-info small">BIN no encontrado en la base de datos local</div>';
            }
        }

        function performBinLookup() {
            const bin = document.getElementById('binInput').value.trim();
            const resultDiv = document.getElementById('binModalResult');
            
            if (bin.length < 6) {
                resultDiv.innerHTML = '<div class="alert alert-warning">El BIN debe tener al menos 6 dígitos</div>';
                return;
            }

            resultDiv.innerHTML = '<div class="text-center"><i class="fas fa-spinner fa-spin"></i> Consultando BIN...</div>';

            // Simular consulta BIN
            setTimeout(() => {
                const binDatabase = {
                    '411111': { brand: 'VISA', type: 'CREDIT', level: 'CLASSIC', bank: 'Chase Bank', country: 'United States' },
                    '555555': { brand: 'MASTERCARD', type: 'CREDIT', level: 'STANDARD', bank: 'Bank of America', country: 'United States' },
                    '378282': { brand: 'AMERICAN EXPRESS', type: 'CREDIT', level: 'GOLD', bank: 'American Express', country: 'United States' }
                };

                const binPrefix = bin.substring(0, 6);
                const info = binDatabase[binPrefix] || {
                    brand: 'UNKNOWN',
                    type: 'CREDIT',
                    level: 'STANDARD',
                    bank: 'Unknown Bank',
                    country: 'Unknown'
                };

                resultDiv.innerHTML = `
                    <div class="card">
                        <div class="card-body">
                            <h6 class="card-title">${info.brand} ${info.type}</h6>
                            <p class="card-text">
                                <strong>BIN:</strong> ${binPrefix}<br>
                                <strong>Banco:</strong> ${info.bank}<br>
                                <strong>País:</strong> ${info.country}<br>
                                <strong>Nivel:</strong> ${info.level}
                            </p>
                        </div>
                    </div>
                `;
            }, 1000);
        }

