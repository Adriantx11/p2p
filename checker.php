        // Checker Form
        document.getElementById('checkerForm')?.addEventListener('submit', function(e) {
            e.preventDefault();
            
            const cards = document.getElementById('cards').value.trim();
            const gateway = document.getElementById('gateway').value;
            
            if (!cards || !gateway) {
                alert('Por favor completa todos los campos requeridos');
                return;
            }

            const cardList = cards.split('\n').filter(line => line.trim() !== '');
            
            if (cardList.length === 0) {
                alert('No se encontraron tarjetas válidas');
                return;
            }

            // Show results section
            document.getElementById('resultsSection').style.display = 'block';
            document.getElementById('totalCards').textContent = cardList.length + ' tarjetas';
            document.getElementById('results').innerHTML = '<div class="text-center py-4"><i class="fas fa-spinner fa-spin fa-2x"></i><br><br>Procesando tarjetas...</div>';

            // Simulate processing
            setTimeout(() => {
                processCards(cardList, gateway);
            }, 1500);
        });

        function processCards(cardList, gateway) {
            let html = '<div class="table-responsive"><table class="table table-striped"><thead><tr><th>Tarjeta</th><th>Estado</th><th>Mensaje</th><th>Gateway</th></tr></thead><tbody>';
            
            cardList.forEach((cardLine, index) => {
                const parts = cardLine.split('|');
                if (parts.length >= 4) {
                    const cardNumber = parts[0].trim();
                    const maskedCard = cardNumber.substring(0, 6) + '****' + cardNumber.substring(cardNumber.length - 4);
                    
                    // Simulate random results
                    const results = ['approved', 'declined', 'insufficient_funds'];
                    const status = results[Math.floor(Math.random() * results.length)];
                    
                    const messages = {
                        'approved': 'Tarjeta válida ✅',
                        'declined': 'Tarjeta declinada ❌',
                        'insufficient_funds': 'Fondos insuficientes 💳'
                    };

                    const statusClass = status === 'approved' ? 'success' : (status === 'declined' ? 'danger' : 'warning');
                    
                    html += `
                        <tr>
                            <td><code>${maskedCard}</code></td>
                            <td><span class="badge bg-${statusClass}">${status.toUpperCase()}</span></td>
                            <td>${messages[status]}</td>
                            <td><span class="badge bg-secondary">${gateway.toUpperCase()}</span></td>
                        </tr>
                    `;
                }
            });
            
            html += '</tbody></table></div>';
            
            // Add summary
            const approved = (html.match(/bg-success/g) || []).length;
            const declined = (html.match(/bg-danger/g) || []).length;
            const insufficient = (html.match(/bg-warning/g) || []).length;
            
            const summary = `
                <div class="row mb-4">
                    <div class="col-md-3">
                        <div class="text-center">
                            <h4 class="text-success">${approved}</h4>
                            <small class="text-muted">Aprobadas</small>
                        </div>
                    </div>
                    <div class="col-md-3">
                        <div class="text-center">
                            <h4 class="text-danger">${declined}</h4>
                            <small class="text-muted">Declinadas</small>
                        </div>
                    </div>
                    <div class="col-md-3">
                        <div class="text-center">
                            <h4 class="text-warning">${insufficient}</h4>
                            <small class="text-muted">Fondos Insuficientes</small>
                        </div>
                    </div>
                    <div class="col-md-3">
                        <div class="text-center">
                            <h4 class="text-info">${cardList.length}</h4>
                            <small class="text-muted">Total Procesadas</small>
                        </div>
                    </div>
                </div>
            `;
            
            document.getElementById('results').innerHTML = summary + html;
        }

